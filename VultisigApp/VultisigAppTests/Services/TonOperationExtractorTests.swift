    func test_decode_returnsNilForEmptyInput() {
        XCTAssertNil(TonMessageBodyDecoder.decode(payload: nil, outerDestination: recipient))
        XCTAssertNil(TonMessageBodyDecoder.decode(payload: "", outerDestination: recipient))
    }

    func test_decode_returnsNilForNonBocGarbage() {
        XCTAssertNil(TonMessageBodyDecoder.decode(payload: "not-a-boc", outerDestination: recipient))
    }

    func test_decode_jettonTransfer() {
        let intent = TonMessageBodyDecoder.decode(
            payload: jettonTransfer,
            outerDestination: recipient
        )
        guard case let .jettonTransfer(transfer) = intent else {
            return XCTFail("expected jettonTransfer, got \(String(describing: intent))")
        }
        XCTAssertEqual(transfer.queryId, "12345")
        XCTAssertEqual(transfer.amount, "100000000")
        XCTAssertEqual(transfer.destination, recipient)
        XCTAssertEqual(transfer.responseDestination, response)
        XCTAssertEqual(transfer.forwardTonAmount, "1000000")
    }

    func test_decode_jettonTransferWithoutResponseDestination() {
        let intent = TonMessageBodyDecoder.decode(
            payload: jettonTransferNoResponse,
            outerDestination: recipient
        )
