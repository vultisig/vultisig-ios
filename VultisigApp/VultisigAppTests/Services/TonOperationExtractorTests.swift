    @MainActor
    private func makeJettonCoin(ticker: String, logo: String, decimals: Int, walletAddress: String) -> Coin {
        let meta = CoinMeta(
            chain: .ton,
            ticker: ticker,
            logo: logo,
            decimals: decimals,
            priceProviderId: "tether",
            contractAddress: "EQAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAu8e",
            isNativeToken: false
        )
        return Coin(asset: meta, address: walletAddress, hexPublicKey: "")
    }

    @MainActor
    func test_extract_resolvesJettonAgainstVaultCoin() {
        // Build a vault with one jetton coin whose `address` matches the
        // outer message destination — that's the user's jetton wallet, the
        // way TonHelper.buildJettonTransfer surfaces it on the keysign.
        let jettonWallet = "EQAvDfWFG0oYX19jwNDNBBL1rKNT9XfaGP9HyTb5nb2Eml6y"
        let coin = makeJettonCoin(ticker: "USDT", logo: "usdt", decimals: 6, walletAddress: jettonWallet)
        let vault = Vault(name: "test")
        vault.coins = [coin]

        let message = TonMessage(to: jettonWallet, amount: "1", payload: jettonTransfer)
        let display = TonOperationExtractor.extract(messages: [message], vault: vault)
        XCTAssertEqual(display?.ticker, "USDT")
        XCTAssertEqual(display?.amountText, "100")
        XCTAssertEqual(display?.display, "100 USDT")
        XCTAssertEqual(display?.logo, "usdt")
    }

    @MainActor
    func test_extract_returnsNilWhenJettonNotInVault() {
        let vault = Vault(name: "test")
        vault.coins = []
        let message = TonMessage(to: response, amount: "1", payload: jettonTransfer)
        XCTAssertNil(TonOperationExtractor.extract(messages: [message], vault: vault))
    }

    @MainActor
    func test_extract_skipsExcessesAndUsesFirstResolvableMessage() {
        let jettonWallet = "EQAvDfWFG0oYX19jwNDNBBL1rKNT9XfaGP9HyTb5nb2Eml6y"
        let coin = makeJettonCoin(ticker: "USDT", logo: "usdt", decimals: 6, walletAddress: jettonWallet)
        let vault = Vault(name: "test")
        vault.coins = [coin]

        // First message is an excesses notification (kind we don't surface),
        // second is a real jetton transfer — extractor should skip the first
        // and resolve against the second.
        let messages = [
            TonMessage(to: recipient, amount: "0", payload: excessesBody),
            TonMessage(to: jettonWallet, amount: "1", payload: jettonTransfer)
        ]
        let display = TonOperationExtractor.extract(messages: messages, vault: vault)
        XCTAssertEqual(display?.ticker, "USDT")
    }
